
public class CarDrawer 
{
	private int xPos;
	private int yPos;
	
	public CarDrawer(int x, int y)
	{
		
		xPos = x;
		yPos = y;
	}
	
	public int getxPos() {
		return xPos;
	}
	public int getyPos() {
		return yPos;
	}
	
	public void setyPos(int yPos) {
		this.yPos = yPos;
	}
	public void setxPos(int xPos) {
		this.xPos = xPos;
	}
	
	
	
	
}
